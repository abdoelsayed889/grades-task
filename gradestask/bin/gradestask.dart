void main()
{
  var grade=40;

  if (grade >= (50) && grade <(60))
  {
    print('D');
  }
   else if (grade >= (60) && grade <(70))
   {
     print('C');
   }
   else if (grade >=70 && grade < 80)
     {
       print('B');
     }
   else if (grade >=80 && grade <90)
     {
       print('A');
     }
   else if(grade >=90 && grade <=100)
     {
       print('A+');
     }
   else
    print('F');
}